public class try11 {
    public static void main(String[] args) {
        byte b = -128; //-128 - 127
        short s = 288; //-32768 - 32767
        int i = 900;
        long l = 90000000L;
        float f = 3.1400000f;
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);

    }
}
